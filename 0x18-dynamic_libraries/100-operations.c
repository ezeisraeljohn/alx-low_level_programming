#include "main.h"

/**
 * add - Function that adds two numbers
 * @a: The first number
 * @b: The second number
 *
 * Return: Return the sum of the numbers
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - Function that subtracts two numbers
 * @a: The first number
 * @b: The second number
 *
 * Return: Returns the difference between the numbers
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - Function that multiplies two numbers
 * @a: The first number
 * @b: The second number
 *
 * Return: The product of the numbers
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - Function that divides two numbers
 * @a: The dividend
 * @b: The divisor
 *
 * Return: The result of the divition
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - Function to get the remainder after division of two numbers
 * @a: The first number
 * @b: The second number
 *
 * Return: The remainder
 */

int mod(int a, int b)
{
	return (a % b);
}
